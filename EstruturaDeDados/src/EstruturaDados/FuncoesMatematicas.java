package Secao4;

public class FuncoesMatematicas {
    public static void main(String[] args) {

        double a = 9.0;
        double b = 4.0;
        double c = - 5.0;

        double valor1 = Math.sqrt(a); //raiz quadrada
        double valor2 = Math.pow(b, 2);// potência
        double valor3 = Math.abs(c); // valor absoluto

        System.out.println(valor1); // saida 3.0
        System.out.println(valor2); // saida 16.0
        System.out.println(valor3); // saida 5.0
        
        // Mais métodos em java.lang.Math
    }
}
