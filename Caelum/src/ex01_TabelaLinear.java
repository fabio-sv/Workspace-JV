public class ex01_TabelaLinear {
    public static void main(String[] args) {

        // matriz unidimensional
        int[] score = {90, 70, 50, 80, 60, 85};

        for (int i = 0; i < score.length; i++){
            System.out.print(score[i] + ", ");

            // out -> 90, 70, 50, 80, 60, 85
        }
    }
}
