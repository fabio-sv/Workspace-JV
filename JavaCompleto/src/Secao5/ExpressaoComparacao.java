package Secao5;

public class ExpressaoComparacao {
    public static void main(String[] args) {

        int x = 5;

        boolean a = x > 0;
        boolean b = x == 3;
        boolean c = 10 <= 30;
        boolean d = x != 2;

        System.out.println(a); //true
        System.out.println(b); //false
        System.out.println(c); //true
        System.out.println(d); //true

    }
}
